// Operadores Lógicos
//  && = And (E) = Todas as expressões precisam ser verdadeiras para retornar true
//  || = Or (Ou) = Se uma expressão for true, retornará true
//  ! = Not (Não) = Nega uma expressão 

const exp1 = true && true && false && true;
console.log(exp1);

const exp2 = false || true;
console.log(exp2);

